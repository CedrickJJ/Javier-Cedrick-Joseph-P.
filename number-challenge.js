/*
    1 Create a function that will return the sum 3 numbers
    2 Create a function that will return the difference of the 2 numbers
    3 Create a function that will return the product of the 2 numbers
    4 Create a function that will return the average of the 2 numbers
*/
function sumThreeNumbers(a, b, c) {
    return a + b + c;
}

console.log(sumThreeNumbers(10, 8, 3));

function differenceOfTwoNumbers(a, b) {
    return a - b;
}
console.log(differenceOfTwoNumbers(2, 8));

function productOfTwoNumbers(a, b) {
    return a * b;
}
console.log(productOfTwoNumbers(8, 8));

function averageOfTwoNumbers(a, b) {
    return (a + b) / 2;
}
console.log(averageOfTwoNumbers(8, 16));
